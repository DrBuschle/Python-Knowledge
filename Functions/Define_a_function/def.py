#### Defining a function

def my_function(variable_to_enter, or_another_variable):	# variable are within brackets, 
								# after the : the definition starts
								# everything within the function needs to be intended
	
	return variable_to_enter + or_another_variable		# 'return' returns the functions output -> it can be stored in a variable
								# in this case variable_to enter plus or_another_variable
								# if you use "print" only, the outcome cannot be stored in a variable