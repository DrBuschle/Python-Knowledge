#### Data types

int	# 1, 2, 3, 4, 5, -1, -2, -3,...			# integer
float	# 1.2, 4.5, -3.3, -0.1,...			# floating point number
bool	# True, False					# boolean
str	# "Hello", "World", "Test", "String",...	# characters
tuple	# (1,2,3,4)					# immutable
list	# [1, "Hello", 3.3, x]				# mutable
dict	# {a:1, b:2, c:3}				# a = key (immutable), 1 = value (mutable)


